<node COLOR="#00b439" CREATED="1223651046170" ID="Freemind_Link_1088630103" MODIFIED="1330591195568" TEXT="As each stage prepares us for the next, the practice of pratyahara creates the setting for dharana, or concentration. Having relieved ourselves of outside distractions, we can now deal with the distractions of the mind itself. No easy task! In the practice of concentration, which precedes meditation, we learn how to slow down the thinking process by concentrating on a single mental object: a specific energetic center in the body, an image of a deity, or the silent repetition of a sound. We, of course, have already begun to develop our powers of concentration in the previous three stages of posture, breath control, and withdrawal of the senses. In asana and pranayama, although we pay attention to our actions, our attention travels. Our focus constantly shifts as we fine-tune the many nuances of any particular posture or breathing technique. In pratyahara we become self-observant; now, in dharana, we focus our attention on a single point. Extended periods of concentration naturally lead to meditation.">
