<node COLOR="#00b439" CREATED="1223651056627" ID="Freemind_Link_311915082" MODIFIED="1330591195586" TEXT="Meditation or contemplation, the seventh stage of ashtanga, is the uninterrupted flow of concentration. Although concentration (dharana) and meditation (dhyana) may appear to be one and the same, a fine line of distinction exists between these two stages. Where dharana practices one-pointed attention, dhyana is ultimately a state of being keenly aware without focus. At this stage, the mind has been quieted, and in the stillness it produces few or no thoughts at all. The strength and stamina it takes to reach this state of stillness is quite impressive. But don&apos;t give up. While this may seem a difficult if not impossible task, remember that yoga is a process. Even though we may not attain the &quot;picture perfect&quot; pose, or the ideal state of consciousness, we benefit at every stage of our progress.">
